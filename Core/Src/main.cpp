/* USER CODE BEGIN 0 */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	static uint16_t progtime;
	switch(GPIO_Pin)
	{

	case GPIO_PIN_12:  //reset
		if (HAL_GetTick()-progtime>1000)
		{
			reset_button=1;
			progtime=HAL_GetTick();
		}
		break;
	case GPIO_PIN_11:  //ok
		if (HAL_GetTick()-progtime>500)
		{
			ok_button=1;
			progtime=HAL_GetTick();
		}
		break;
	case GPIO_PIN_7:  //encoder
		if (HAL_GetTick()-progtime>500)
		{
			kill_read=1;
			progtime=HAL_GetTick();
		}
		break;
	}
}

